count = 0

puts "Привет! Вам предгается пройти тест. Честно отвечайте на вопросы да или нет"

for item in questions do
	puts item
	choice = gets.chomp

	until (choice == "да" || choice == "ДА" || choice == "нет" || choice == "НЕТ")
		sleep 0.5
		puts "Принимаются только ответы ДА или НЕТ (да или нет)"
		choice = gets.chomp
	end

	if (choice == "да")
		count += 1
	end
end

